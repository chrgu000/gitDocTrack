    /**
     * Compress by quality,  and generate image to the path specified
     *
     * @param image
     * @param outPath
     * @param maxSize target will be compressed to be smaller than this size.(kb)
     * @throws IOException
     */
    public static void compressAndGenImage(Bitmap image, String outPath, int maxSize) throws IOException {
        ByteArrayOutputStream os = new ByteArrayOutputStream();
        // scale
        int options = 80;
        // Store the bitmap into output stream(no compress)
        image.compress(Bitmap.CompressFormat.JPEG, options, os);
        // Compress by loop
        while (os.toByteArray().length / 1024 > 100) {
            int i=os.toByteArray().length;
            // Clean up os
            os.reset();
            // interval 10
            options -= 10;
            image.compress(Bitmap.CompressFormat.JPEG, options, os);
        }

        // Generate compressed image file
        FileOutputStream fos=null;
        try{
            fos = new FileOutputStream(outPath);
            fos.write(os.toByteArray());
            fos.flush();
        }catch (Exception e){
            e.printStackTrace();
        } finally {
            if(fos!=null) {
                fos.close();
            }
        }
}
    public static Bitmap scale(Bitmap bitmap, float scale) {
        //获得Bitmap的高和宽
        int bmpWidth = bitmap.getWidth();
        int bmpHeight = bitmap.getHeight();
        //计算出这次要缩小的比例
        int scaleWidth = (int) (bmpWidth * scale);
        int scaleHeight = (int) (bmpHeight * scale);
        Matrix matrix = new Matrix();
        matrix.postScale(scale, scale); //长和宽放大缩小的比例
        Bitmap resizeBmp = Bitmap.createBitmap(bitmap, 0, 0, bmpWidth, bmpHeight, matrix, true);
        return resizeBmp;
    }
